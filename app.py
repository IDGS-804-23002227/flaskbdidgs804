from flask import Flask, render_template, request, redirect, url_for
from flask_wtf.csrf import CSRFProtect
from config import DevelopmentConfig
import forms
from models import db, Alumnos

app = Flask(__name__)
app.config.from_object(DevelopmentConfig)

csrf = CSRFProtect(app)
db.init_app(app)

with app.app_context():
    db.create_all()
@app.errorhandler(404)
def page_not_fount(e):
	return render_template("404.html"),404

@app.route("/", methods=["GET", "POST"])
@app.route("/index")
def index():
    create_form= forms.UsuarioForm(request.form)
    #tem = Alumnos.query('select * from alumnos')
    alumno=Alumnos.query.all()
    return render_template("index.html",form=create_form, alumno=alumno)

@app.route("/alumnos", methods=["GET", "POST"])
def alumnos():
    create_form = forms.UsuarioForm()

    if create_form.validate_on_submit():
        alum = Alumnos(
            nombre=create_form.nombre.data,
            apaterno=create_form.apaterno.data,
            email=create_form.email.data
        )
        db.session.add(alum)
        db.session.commit()
        return redirect(url_for('index'))

    return render_template("alumnos.html", form=create_form)

@app.route("/detalles", methods=['GET', 'POST'])
def detalles():
    create_form=forms.UsuarioForm(request.form)
    if request.method=='GET':
        id=request.args.get('id')
        alum1 = db.session.query(Alumnos).filter(Alumnos.id==id).first()
        id=request.args.get('id')
        nombre=alum1.nombre
        apaterno=alum1.apaterno
        email=alum1.email
    
    return render_template('detalles.html', id=id, nombre=nombre, apaterno=apaterno, email=email, form=create_form)


if __name__ == '__main__':
    app.run()
    
